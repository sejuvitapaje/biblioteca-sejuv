// ‚≠ê NOVO: VERIFICA SE PRECISA SINCRONIZAR
async function verificarSincronizacao() {
    if (!db || sistema.sincronizacao.emAndamento || !sistema.cache.carregado) return;
    
    // Verifica a cada 5 minutos no m√°ximo
    if (sistema.sincronizacao.ultimaVerificacao && 
        Date.now() - sistema.sincronizacao.ultimaVerificacao < 300000) {
        return;
    }
    
    sistema.sincronizacao.ultimaVerificacao = Date.now();
    
    try {
        console.log("üîç Verificando sincroniza√ß√£o...");
        
        // ‚≠ê LEITURA R√ÅPIDA: CONTA APENAS O TOTAL DE LIVROS (1 LEITURA LEVE)
        const contadorSnapshot = await db.collection('livros').get();
        const totalFirebase = contadorSnapshot.size;
        const totalLocal = sistema.cache.livros.length;
        
        console.log(`üìä Compara√ß√£o: Local ${totalLocal} vs Firebase ${totalFirebase}`);
        
        if (totalFirebase !== totalLocal) {
            console.log(`üîÑ Sincroniza√ß√£o necess√°ria! (Diferen√ßa: ${Math.abs(totalFirebase - totalLocal)} livros)`);
            mostrarNotificacaoSincronizacao(totalFirebase - totalLocal);
            
            // ‚≠ê SINCRONIZA AUTOMATICAMENTE SE DIFEREN√áA PEQUENA
            if (Math.abs(totalFirebase - totalLocal) <= 10) {
                console.log("üîÑ Sincroniza√ß√£o autom√°tica iniciada...");
                sincronizarDados();
            }
        } else {
            console.log("‚úÖ Dados sincronizados");
        }
        
    } catch (error) {
        console.log("‚ùå Erro na verifica√ß√£o de sincroniza√ß√£o:", error);
    }
}

// ‚≠ê NOVO: MOSTRA NOTIFICA√á√ÉO DE SINCRONIZA√á√ÉO
